class Product {
  final String? pID;
  final String? name;
  final DateTime? originDateTime;
  final double? price;
  final double? rating;
  final Map? sizes;
  final List? tags;
  final List? images;
  final double? discount;
  final String? description;
  final List? colors;
  final String? baseText;

  Product({
    this.pID,
    this.name,
    this.originDateTime,
    this.price,
    this.rating,
    this.sizes,
    this.tags,
    this.images,
    this.discount,
    this.description,
    this.colors,
    this.baseText,
  });
}
